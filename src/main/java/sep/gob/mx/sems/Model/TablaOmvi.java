package sep.gob.mx.sems.Model;




public class TablaOmvi {
	
	
	private Integer ID_OMVI;
	
	
	private String FULL_NAME;
	
	
	private String AREA_OF_ADSCRIPTION;
	
	
	private String MOT_COMISION;
	
	
	private Integer TOTAL_IMPORT;
	
	public Integer getID_OMVI() {
		return ID_OMVI;
	}
	public void setID_OMVI(Integer iD_OMVI) {
		ID_OMVI = iD_OMVI;
	}
	public String getFULL_NAME() {
		return FULL_NAME;
	}
	public void setFULL_NAME(String fULL_NAME) {
		FULL_NAME = fULL_NAME;
	}
	public String getAREA_OF_ADSCRIPTION() {
		return AREA_OF_ADSCRIPTION;
	}
	public void setAREA_OF_ADSCRIPTION(String aREA_OF_ADSCRIPTION) {
		AREA_OF_ADSCRIPTION = aREA_OF_ADSCRIPTION;
	}
	public String getMOT_COMISION() {
		return MOT_COMISION;
	}
	public void setMOT_COMISION(String mOT_COMISION) {
		MOT_COMISION = mOT_COMISION;
	}
	public Integer getTOTAL_IMPORT() {
		return TOTAL_IMPORT;
	}
	public void setTOTAL_IMPORT(Integer tOTAL_IMPORT) {
		TOTAL_IMPORT = tOTAL_IMPORT;
	}
	
	
}
